    private void ObjectCreation()
    {
        user = new UserInfo();
        
        user.setName(tfName.getText());
        user.setAddress(tfAddress.getText());
        user.setPhoneNo(tfPhone.getText());
        user.setType(tfType.getText());
        try{
            user.setCustomerId(Integer.parseInt(tfId.getText()));
        }
        catch(Exception ex)
        {
            user.setCustomerId(0);
        }
        
    }
    
    private void table_customerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_customerMouseClicked
       
        user = new UserInfo();
        int row = tableCustomer.getSelectedRow();
        displayToTextField(row);
        //ObjectCreation();
        btnEditCustomer.setEnabled(true);
        btnDeleteCustomer.setEnabled(true);
        btnAddCustomer.setEnabled(false);
        
    }//GEN-LAST:event_table_customerMouseClicked

