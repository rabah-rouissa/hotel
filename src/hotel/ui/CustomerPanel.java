    private void btn_deleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteCustomerActionPerformed
        int row  = tableCustomer.getSelectedRow();
        int selectedUserId = (int) tableCustomer.getModel().getValueAt(row, 0);
        try {
            db.deleteCustomer(selectedUserId);
            
        } catch (SQLException ex) {
            Logger.getLogger(CustomerPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        populateWithCustomerData();
        emptyField();
        
    }//GEN-LAST:event_btn_deleteCustomerActionPerformed

    private void tf_phoneKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_phoneKeyTyped
         char c = evt.getKeyChar();
        
        if(!(Character.isDigit(c) || c== KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE ))
        {
            evt.consume();
        }
    }//GEN-LAST:event_tf_phoneKeyTyped

    
    private void emptyField()
    {
         btnAddCustomer.setEnabled(true);
        btnEditCustomer.setEnabled(false);
        btnDeleteCustomer.setEnabled(false);
        
        user = new UserInfo();
        
        tfAddress.setText("");
        tfId.setText("");
        tfName.setText("");
        tfPhone.setText("");
        tfType.setText("");
        tableCustomer.clearSelection();
    }
    
    private void displayToTextField(int row)
{
    tfId.setText(tableCustomer.getModel().getValueAt(row, 0)+"");
    tfName.setText((String) tableCustomer.getModel().getValueAt(row, 1));
    tfAddress.setText((String) tableCustomer.getModel().getValueAt(row, 2));
    tfPhone.setText((String) tableCustomer.getModel().getValueAt(row, 3));
    tfType.setText((String) tableCustomer.getModel().getValueAt(row, 4));
}
